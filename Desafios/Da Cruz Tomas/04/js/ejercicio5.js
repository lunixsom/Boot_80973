let primerNumero = parseFloat(prompt("Introduce el primer número"));
let segundoNumero = parseFloat(prompt("Introduce el segundo número"));
let tercerNumero = parseFloat(prompt("Introduce el tercer número"));

let promedio = (primerNumero + segundoNumero + tercerNumero) / 3;
console.log(`El promedio de ${primerNumero} + ${segundoNumero} + ${tercerNumero} es igual a ${promedio}`);